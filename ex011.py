n1 = int(input('Digite um número inteiro: '))
n2 = int(input('Digite outro número inteiro: '))
n3 = float(input('Digite um número real: '))
conta1 = (n1*2) * (n2/2)
conta2 = n1*3 + n3
conta3 = n3**3
print(f'O produto do dobro do primeiro número com a metade do segundo é {conta1:.2f}')
print(f'A soma do triplo do primeiro número com o terceiro é {conta2:.2f}')
print(f'O terceiro número elevado ao cubo é {conta3:.2f}')
